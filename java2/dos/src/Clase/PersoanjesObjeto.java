package Clase;

public class PersoanjesObjeto {
    String nombre;
    String anime;
    public PersoanjesObjeto(String nombre, String anime) {
        this.nombre = nombre;
        this.anime = anime;
    }
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public String getAnime() {
        return anime;
    }
    public void setAnime(String anime) {
        this.anime = anime;
    }

}
